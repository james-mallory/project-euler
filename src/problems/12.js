/*
 
Highly divisible triangular number

Problem 12

The sequence of triangle numbers is generated by adding the natural numbers. So the 7th triangle number would be 1 + 2 + 3 + 4 + 5 + 6 + 7 = 28. The first ten terms would be:

1, 3, 6, 10, 15, 21, 28, 36, 45, 55, ...

Let us list the factors of the first seven triangle numbers:

     1: 1
     3: 1,3
     6: 1,2,3,6
    10: 1,2,5,10
    15: 1,3,5,15
    21: 1,3,7,21
    28: 1,2,4,7,14,28

We can see that 28 is the first triangle number to have over five divisors.

What is the value of the first triangle number to have over five hundred divisors?

solution: 76576500

 */

import { getNthTriangularNumber } from '../utils/math';
import { getFactors } from '../utils/factoring';

const TRIANGLE_NUM_FACTORS = 500;

export default function solve() {
    let number = 0;
    let n = 1;
    while (number === 0) {
        const triangleNum = getNthTriangularNumber(n);
        if (triangleNum > TRIANGLE_NUM_FACTORS * 2 - 1) {
            const factors = getFactors(triangleNum);
            if (factors.length > TRIANGLE_NUM_FACTORS) number = triangleNum;
        }
        n++;
    }
    console.log(`Solution #12: ${number}`);
}
